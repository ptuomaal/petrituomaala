let currentLang = 'fi';

function initLang() {
    const params = new URLSearchParams(window.location.search);
    const urlLang = params.get('lang');
    const storedLang = localStorage.getItem('ptu-lang');

    currentLang = urlLang || storedLang || 'fi';
    if (currentLang !== 'fi' && currentLang !== 'en') {
        currentLang = 'fi';
    }

    localStorage.setItem('ptu-lang', currentLang);
    document.documentElement.lang = currentLang;
}

function getLang() {
    return currentLang;
}

function setLang(lang) {
    if (lang !== 'fi' && lang !== 'en') return;
    currentLang = lang;
    localStorage.setItem('ptu-lang', lang);
    document.documentElement.lang = lang;

    const url = new URL(window.location);
    if (lang === 'fi') {
        url.searchParams.delete('lang');
    } else {
        url.searchParams.set('lang', lang);
    }
    history.replaceState(null, '', url);

    document.dispatchEvent(new CustomEvent('lang-changed'));
}

function t(key) {
    return translations[currentLang][key] || translations['fi'][key] || key;
}

// Self-initialize on import
initLang();

export { t, getLang, setLang };
